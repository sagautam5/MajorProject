package FeatureExtraction.org.classes;
/**
 * 
 * A 3 dimensional point which has been used to represent the 
 * each voxel of MRI scan in 3d space.
 *
 * @author Sagar Gautam
 * @version 1.0
 * @since 2016-02-20
 */
public class Vector{

    int x;
    int y;
    int z;

    Vector(){
    
        x = 0;
        y = 0;
        z = 0;
    }

    Vector(int x, int y, int z){
    
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
